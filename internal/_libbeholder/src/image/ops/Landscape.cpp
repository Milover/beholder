bool Landscape::execute(const cv::Mat& in, cv::Mat& out) const
{
	if (in.cols > in.rows)
	{
		return true;
	}
	cv::rotate(in, out, cv::ROTATE_90_CLOCKWISE);

	return true;
}

bool Landscape::execute
(
	const cv::Mat& in,
	cv::Mat& out,
	const std::vector<Result>&
) const
{
	return execute(in, out);
}

